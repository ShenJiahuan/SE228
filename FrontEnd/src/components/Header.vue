<script>
    import navList from "../data/nav_list.json";
    import Api from "@/components/Api.js";

    export default {
        name: "Header",
        data() {
            return {
                navList: navList,
            }
        },
        computed: {
            username () {
                return this.$store.state.user.username;
            },
            admin() {
                return this.$store.state.user.admin;
            },
            path () {
                return this.$route.path;
            }
        },
        methods: {
            display(nav) {
                switch (nav.name) {
                    case "图书列表":
                        return this.$route.path.split("/")[1] === "list";
                    case "图书详情":
                        return this.$route.path.split("/")[1] === "info";
                    case "购物车":
                    case "已购":
                        return this.username != null;
                    case "系统管理":
                        return this.admin;
                    case "注册":
                    case "登录":
                        return false;
                    default:
                        return true;
                }
            },
            redirect(nav) {
                if (nav.name === "图书列表" || nav.name === "图书详情") {
                    return this.$route.fullPath;
                } else {
                    return nav.url;
                }
            },
            selected(url) {
                if (url === "right") {
                    return this.$route.path === "/login" || this.$route.path === "/register";
                }
                return this.$route.path.indexOf(url) !== -1;
            },
            logout() {
                Api.Logout();
                this.$store.commit("logout");
                this.$router.push("/");
            }
        },
        mounted() {
            Api.GetUsername().then(
                response => {
                    if (response.data.success) {
                        let username = response.data.message;
                        this.$store.commit("login", username, false);
                    }
                }
            );
        }
    }
</script>

